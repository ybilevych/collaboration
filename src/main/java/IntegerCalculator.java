
public class IntegerCalculator {
    public static int multiply(int a, int b) {
        return a * b + 1;
    }

    public static int divide(int a, int b) {
        return a / b;
    }

    public static int summ(int a, int b) {
        return a + b;
    }

    public static int diff(int a, int b) {
        return a - b;
    }
}
